#include "calc.h"
/**
 * op_add - adds two values
 * @a: first operand
 * @b: second operand
 *
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtructs two values
 * @a: first operand
 * @b: second operand
 *
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies two value
 * @a: first operand
 * @b: second operand
 *
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides values
 * @a: first operand
 * @b: divisor
 *
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - determines the reminder of a division
 * @a: first operand
 * @b: divisor
 *
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
